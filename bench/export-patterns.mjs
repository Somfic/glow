#!/usr/bin/env node
// Generates src/lib/glow/patterns.ts from the selected benchmark variants.
//
// Several variants import a shared GLSL prelude from a sibling file, so the
// sources are resolved by importing the module and reading the final assembled
// strings rather than by copying text out of the files.

import { writeFile } from 'node:fs/promises';
import { join, dirname } from 'node:path';
import { fileURLToPath, pathToFileURL } from 'node:url';

const BENCH = dirname(fileURLToPath(import.meta.url));
const OUT = join(BENCH, '..', 'src', 'lib', 'glow', 'patterns.ts');

// pattern name -> [variant file, description, time scale]
//
// The time scale normalises animation rate: every shader advances `u_time` at
// its own internal rate, so without this `speed={1}` means something different
// on each pattern. The component multiplies its time step by this factor, so a
// given `speed` value reads as the same rate of motion everywhere.
//
// Starting values came from a measured half-decorrelation time per pattern
// (bench/run.mjs --speed-probe), but the FINAL values are Lucas's, tuned by eye
// against all twelve running side by side. Perceived speed is not the same
// thing as pixel-change rate — the metric over-weights fine flickering detail —
// so where the two disagree, the eye wins.
// `fold` is handled separately: it stays in foldGradientShader.ts because it is
// the one shader that depends on the LUT and the banded renderer.
const SELECTED = [
	['aurora', 'pat-flow-aurora', 'Northern-lights curtains — meandering ribbons with fine vertical rays', 0.079],
	['curl', 'pat-flow-curl', 'Silky curl-noise streamlines; dye smeared along a flowing vector field', 0.39],
	['ink', 'pat-flow-ink', 'Ink in water — thresholded diffusion fronts with feathered tendrils', 0.19],
	['oilfilm', 'pat-flow-oilfilm', 'Iridescent liquid light; thin-film interference over a flowing surface', 0.26],
	['marble', 'pat-org-marble', 'Polished stone slab with crossing calcite vein families', 0.192],
	['caustics', 'pat-opt-caustics', 'Sharp filament network shimmering over a depth-graded pool', 0.084],
	['prism', 'pat-opt-prism', 'Light-slits fanned into spectra by a warped glass wedge', 0.845],
	['soapfilm', 'pat-opt-soapfilm', 'Iridescent film with thin order-contours, marbled by convection', 2.005],
	['dither', 'pat-gfx-dither', 'Chunky ordered dither; a drifting light posterised into woven shells', 0.875],
	['halftone', 'pat-gfx-halftone', 'Screen-printed halftone, three inks on classic separation angles', 0.995],
	['mesh', 'pat-gfx-mesh', 'Soft gradient mesh — one drifting control point per palette colour', 0.57]
];

// GLSL goes into a TS template literal, so backticks and ${ must not terminate it.
const embed = (src) => src.replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');

let baseVertex = null;
const out = [];

for (const [name, file, blurb, scale] of SELECTED) {
	const mod = (await import(pathToFileURL(join(BENCH, 'variants', file + '.js')).href)).default;
	if (!mod?.fragmentShader) throw new Error(`${file} has no fragmentShader`);
	baseVertex ??= mod.vertexShader;
	if (mod.vertexShader.trim() !== baseVertex.trim()) {
		throw new Error(`${file} uses a non-standard vertex shader; the component assumes one shared`);
	}
	out.push({ name, file, blurb, scale, fs: mod.fragmentShader });
}

const ts = `// AUTO-GENERATED by bench/export-patterns.mjs — do not edit by hand.
//
// Alternative background patterns for <Glow pattern="…" />. Each is a
// self-contained single-pass fragment shader sharing the component's uniform
// block, so every pattern responds to the same \`colors\`, \`zoom\`, \`rotation\`
// and \`speed\` props. The default \`fold\` pattern is not here — it lives in
// foldGradientShader.ts because it is the one shader that needs the baked noise
// LUT and the banded two-pass renderer.

export type PatternName =
	| 'fold'
${out.map((o) => `	| '${o.name}'`).join('\n')};

export const PATTERN_NAMES: PatternName[] = [
	'fold',
${out.map((o) => `	'${o.name}'`).join(',\n')}
];

// One-line descriptions, handy for docs and demo pages.
export const PATTERN_DESCRIPTIONS: Record<PatternName, string> = {
	fold: 'Soft folded sheets of light — the original Glow gradient',
${out.map((o) => `	${o.name}: ${JSON.stringify(o.blurb)}`).join(',\n')}
};

// Per-pattern animation-rate normalisation, so \`speed={1}\` is the same rate of
// motion on every pattern. Tuned by eye with all twelve side by side; \`fold\` is
// the reference at 1.0.
export const PATTERN_TIME_SCALE: Record<PatternName, number> = {
	fold: 1,
${out.map((o) => `	${o.name}: ${o.scale}`).join(',\n')}
};

${out
	.map(
		(o) => `// ${o.blurb}
// Source: bench/variants/${o.file}.js
const ${o.name.toUpperCase()} = /* glsl */ \`${embed(o.fs)}\`;`
	)
	.join('\n\n')}

// Fragment shader per pattern. \`fold\` is absent on purpose — the component
// imports it directly so that tree of code stays with its LUT.
export const PATTERN_SHADERS: Record<Exclude<PatternName, 'fold'>, string> = {
${out.map((o) => `	${o.name}: ${o.name.toUpperCase()}`).join(',\n')}
};
`;

await writeFile(OUT, ts);
console.log(`wrote ${OUT}\n${out.length} patterns, ${(ts.length / 1024).toFixed(0)} KB`);
